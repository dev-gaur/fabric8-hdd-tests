echo "$ZABBIX_HOST $METRIC_PREFIX-rt_75 $ZABBIX_TIMESTAMP ${DISTR[6]}" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-rt_80 $ZABBIX_TIMESTAMP ${DISTR[7]}" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-rt_90 $ZABBIX_TIMESTAMP ${DISTR[8]}" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-rt_95 $ZABBIX_TIMESTAMP ${DISTR[9]}" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-rt_98 $ZABBIX_TIMESTAMP ${DISTR[10]}" >> $ZABBIX_LOG_FILE
