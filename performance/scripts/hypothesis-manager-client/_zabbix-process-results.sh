#!/bin/bash

source _setenv.sh

if [ ! -f "$1-report_requests.csv" ]; then
	echo "File '$1' not found! Skipping Zabbix reporting... !!!"
	exit 0
fi

INPUT=$1
ENDPOINT=${2-"GET","/api/v1.0/experiments"}
METRIC_PREFIX=${3-hypothesis-manager-client}
ZABBIX_TIMESTAMP=`date +%s`
ZABBIX_LOG_FILE=$1-zabbix.log

VALUES=(`cat $INPUT-report_requests.csv | grep -F "$ENDPOINT" | cut -d ',' -f 3-10 | tr ',' ' '`)
VAL_REQ=$((${VALUES[0]} + ${VALUES[1]}))
VAL_FAIL=${VALUES[1]}
VAL_FAIL_RATE=`echo "100*$VAL_FAIL/$VAL_REQ" | bc -l`
VAL_MED=${VALUES[2]}
VAL_AVG=${VALUES[3]}
VAL_MIN=${VALUES[4]}
VAL_MAX=${VALUES[5]}

echo "$ZABBIX_HOST $METRIC_PREFIX-failed $ZABBIX_TIMESTAMP $VAL_FAIL" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-fail_rate $ZABBIX_TIMESTAMP $VAL_FAIL_RATE" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-rt_average $ZABBIX_TIMESTAMP $VAL_AVG" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-rt_median $ZABBIX_TIMESTAMP $VAL_MED" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-rt_min $ZABBIX_TIMESTAMP $VAL_MIN" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-rt_max $ZABBIX_TIMESTAMP $VAL_MAX" >> $ZABBIX_LOG_FILE
echo "$ZABBIX_HOST $METRIC_PREFIX-users $ZABBIX_TIMESTAMP $USERS" >> $ZABBIX_LOG_FILE

